    public static double potencia(double base, int exponente) {
         if (exponente == 0) { //caso base
            return 1; //10^0 = 1
        } else if (exponente == 1) { //caso base
            return base; //10^1 = 10
        } else if (exponente < 0) { //Exponente negativo
            return potencia(base, exponente + 1) / base;

        } else 
{  //Exponente positivo
            return base * potencia(base, exponente - 1);
        }
     }
    
    public static double potenciad(int base, int exponente) {
         if (exponente == 0) { //caso base
            return 1; //10^0 = 1
        } else if (exponente == 1) { //caso base
            return base; //10^1 = 10
        } else if (exponente < 0) { //Exponente negativo
            return potencia(base, exponente + 1) / base;

        } else 
{  //Exponente positivo
            return base * potencia(base, exponente - 1);
        }
     }
    
    public static int factorialRecursivo(int numero){
         int res;
         if(numero==1){
            //Se termina la recursiÃ³n
            return 1;
        }
else{

      //Se llama asi misma la funcion, con el valor del numero menos 1
           
res = numero * factorialRecursivo(numero-1);
        
}
       //Devolvemos el valor
        
return res;
      }
